      let { info = {} } = this;
      let { content = false, bindData = {} } = info || {};
      let { deviceId = "" } = bindData || {};
      if (!deviceId) {
        info.content = !content;
        return;
      }
      let point = pointCode;
      let value = !content ? 1 : 0;
      $vm.$emit("control", {
        deviceId,
        point,
        value,
        callback: flag => {
          if (flag) {
            info.content = !content;
          } else {
            info.content = content; //如果取消则重置结果
          }
        }
      });
    },
